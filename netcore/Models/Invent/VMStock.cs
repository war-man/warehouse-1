using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Threading.Tasks;

namespace netcore.Models.Invent
{
    public class VMStock
    {
        [Display(Name = "Product")]
        public string Product { get; set; }
        [Display(Name = "Warehouse")]
        public string Warehouse { get; set; }
        [Display(Name = "Qty PO")]
        public float QtyPO { get; set; }//Lệnh nhập hàng
        [Display(Name = "Qty Receiving")]
        public float QtyReceiving { get; set; }//Nhập hàng
        [Display(Name = "Qty SO")]
        public float QtySO { get; set; } //Lệnh xuất hàng
        [Display(Name = "Qty Shipment")]
        public float QtyShipment { get; set; }//Xuất hàng
        [Display(Name = "Qty Transfer In")]
        public float QtyTransferIn { get; set; }//Điều chuyển kho vào
        [Display(Name = "Qty Transfer Out")]
        public float QtyTransferOut { get; set; }//Điều chuyển kho ra
        [Display(Name = "Qty On Hand")]
        public float QtyOnhand { get; set; }//Tồn thực tế
    }
}
